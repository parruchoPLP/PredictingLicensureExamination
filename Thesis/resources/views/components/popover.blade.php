        <p>What is the purpose of the performance metrics?</p>
    </div>
    <div class="p-4 font-normal text-justify">
        <p>
        The performance metrics of <b>accuracy, precision, recall,</b> and <b>F1 score</b> are critical for evaluating the effectiveness of the predictive model in assessing student outcomes. <b>Accuracy</b> measures the overall percentage of correct predictions made by the model, providing a general indication of its performance. <b>Precision</b> reflects the proportion of true positive results among all positive predictions, showcasing the model's reliability in identifying successful outcomes. <b>Recall</b> measures the ability to capture all relevant instances, highlighting how effectively the model identifies true positives. The <b>F1 score</b>, which is the harmonic mean of precision and recall, offers a balanced measure of both metrics.
        <br> <br> Analyzing these performance metrics allows educators to assess the predictive capabilities of the model, ensuring interventions are based on reliable data. Understanding these metrics is essential for refining educational strategies and improving student success in the Electronics Engineers Licensure Examination.</p>
    </div>
